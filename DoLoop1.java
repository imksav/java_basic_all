public class DoLoop1 {
	public static void main(String[] args) {
//		Do.....Loop.....conditions..............
		
		int i = 1;
//		conditions..............
		do {
			System.out.print(i +"\t");
			i++;
		}
		while(i < 5);
		
		int j = 1;
//		conditions..............
		do {
			System.out.println("Another is " +j);
			j++;
		}
		while(j > 5);
		
		
		
		
	}

}